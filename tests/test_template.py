def check_api_docs(project):
    """Check that 'make docs' builds a populated API page.

    The template ships an example ``hello_world`` function; its name and
    docstring must appear in the built HTML, confirming the auto-generated API
    documentation is non-empty (for both the MyST and the reStructuredText
    docstring paths).
    """
    res = make(project, 'docs')
    assert res.returncode == 0
    html_dir = Path(project.join('docs', '_build', 'html'))
    html = '\n'.join(p.read_text() for p in html_dir.rglob('*.html'))
    assert 'hello_world' in html
    assert 'friendly greeting' in html


