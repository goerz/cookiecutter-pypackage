def check_custom_metadata(project):
    """Check that the project was generated with the custom metadata from the
    CONFIGURATION"""
    assert project.join('src', 'mypypackage').isdir()
    with open(project.join('pyproject.toml')) as in_fh:
        pyproject = in_fh.read()
        assert "This project is generated with custom metadata" in pyproject
        assert 'name = "mypypackage"' in pyproject
        assert 'version = "1.0.0-dev"' in pyproject
        assert (
            '{ name = "The Architect", '
            'email = "architect@mail.michaelgoerz.net" }'
        ) in pyproject
        assert (
            'Homepage = "https://github.com/qucontrol/mypypackage"'
            in pyproject
        )
    with open(project.join('src', 'mypypackage', '__init__.py')) as in_fh:
        init_py = in_fh.read()
        assert 'version("mypypackage")' in init_py
        assert "My-Py-Package" in init_py


